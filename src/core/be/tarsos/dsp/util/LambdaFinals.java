package be.tarsos.dsp.util;

public class LambdaFinals
{
    public static class LambdaFloat
    {
        public float value = 0.f;

        public LambdaFloat()
        {
        }

        public LambdaFloat(float value)
        {
            this.value = value;
        }
    }

    public static class LambdaInt
    {
        public int value = 0;

        public LambdaInt()
        {
        }

        public LambdaInt(int value)
        {
            this.value = value;
        }
    }

    public static class LambdaBoolean
    {
        public boolean value = false;

        public LambdaBoolean()
        {
        }

        public LambdaBoolean(boolean value)
        {
            this.value = value;
        }
    }
}
